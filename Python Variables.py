# Python Variables

# Variables store information
name = "David"
age = 20
height = 1.78
is_student = True

# Display variables
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Student:", is_student)

# Reassign a variable
age = 21
print("New age:", age)

